    // Action sur le bouton modifier du panel produits
    private void ams_btn_modifier_produitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ams_btn_modifier_produitActionPerformed
        boolean update = false;
        if(ams_tf_id_produit.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Recherchez un produit pour pouvoir le modifier", "Gestion de stock - AEKI", JOptionPane.INFORMATION_MESSAGE);
        } else {
            if((ams_tf_nom_produit.getText().equals("")) || (ams_tf_prix.getText().equals("")) || (ams_tf_stock.getText().equals(""))) {
                JOptionPane.showMessageDialog(null, ("""
                Veuillez remplir les champs obligatoires :
                Nom produit
                Prix
                Quantit\u00e9 en stock"""
                ), "Modification de produit", JOptionPane.INFORMATION_MESSAGE);
            } else {
                String nom_cat = ams_cbb_categorie.getSelectedItem().toString();
                String nom_souscat = ams_cbb_sous_cat.getSelectedItem().toString();
                int id_produit = Integer.parseInt(ams_tf_id_produit.getText());
                String nom_produit = ams_tf_nom_produit.getText();
                double prix = Double.parseDouble(ams_tf_prix.getText());
                String description = ams_ta_description.getText();
                boolean en_promotion = ams_ckb_en_promotion.isSelected();
                int promo = Integer.parseInt(ams_cbb_tx_promo.getSelectedItem().toString());
                double prix_reduit = Double.parseDouble(ams_tf_prix_reduit.getText());
                int stock = Integer.parseInt(ams_tf_stock.getText());
                String img = ams_ta_lien_img.getText();
                Produit p = new Produit(id_produit, nom_produit, description, prix, en_promotion, promo, prix_reduit, stock, img);
                int input = JOptionPane.showConfirmDialog(null, (
                    "Vous souhaitez modifier le produit suivant :\n" +
                    "Nom produit : " + nom_produit + "\n" +
                    "Categorie : " + nom_cat + " " +
                    "Sous-categorie : " + nom_souscat + "\n" +
                    "Description : " + description + "\n" +
                    "Prix : " + prix + " \u20ac " +
                    "En promotion : " + ((ams_ckb_en_promotion.isSelected()) ? "OUI" : "NON") + " " +
                    "Taux promo : " + promo + " % " +
                    "Prix reduit : " + prix_reduit + " \u20ac\n" +
                    "Quantite en stock : " + stock
                ), "Confirmation de modifcation de produit",JOptionPane.YES_NO_OPTION);
                if (input == JOptionPane.YES_OPTION) {
                    update = updateProduit(p);
                }
                if (update == true) {
                    JOptionPane.showMessageDialog(null, "Le produit a été modifié avec succès", "Gestion de stock - AEKI", JOptionPane.INFORMATION_MESSAGE);
                    int i = 0;
                    while(!ams_tf_nom_produit.getText().equals((String)modelTableProduit.getValueAt(i, 1))){
                        i++;
                    }
                    
                    String Spromo = Integer.toString(promo);
                    modelTableProduit.setValueAt(nom_produit, i, 1);
                    modelTableProduit.setValueAt(description, i, 2);
                    modelTableProduit.setValueAt(prix, i, 3);
                    modelTableProduit.setValueAt(en_promotion, i, 4);
                    modelTableProduit.setValueAt(Spromo, i, 5);
                    modelTableProduit.setValueAt(prix_reduit, i, 6);
                    modelTableProduit.setValueAt(stock, i, 7);
                    
                    ams_effacer_champs();
                    r_tf_nom_produit.setText("");
                    modelTableProduit.fireTableDataChanged();
                }
            }
        }
    }//GEN-LAST:event_ams_btn_modifier_produitActionPerformed

